# List - Sequence Datatype (inbuilt)
# Python has many inbuilt datatypes, list is one of them
# print(help(list))

# List is a collection of elements which is mutable
# Mutable - Items inside a list can be modified or changed or updated or deleted
# It can be homogeneous or heterogeneous
# Homogeneous - Same datatype
# Ex: list1 = [10,20,30,40]

# Heterogeneous - Different datatype
# Ex: list2 = [5,5.6,'hello',5+6j]

# List is created inside a square bracket
# Syntax: list_name = [list_elements]
# How to create an empty list
list1 = []
print(type(list1))
# <class 'list'>

# To find the length of a list
l1 = [10, 20, 40, 'hi', 'welcome', 5.9]
print(len(l1))
# 6

# How to create a list using a constructor
l2 = list()
print(type(l2))
# <class 'list'>

