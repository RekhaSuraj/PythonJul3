# Number 
# 1. int - It is a whole number, Ex: 1,2,4,-1,-2,3 (positive and negative numbers)

# type() - It is an inbuilt method to check the datatype of the object

# syntax: type(object)

l = 20
print(type(l))
# <class 'int'>

# 2. float (decimal) - It contains decimal points like 4.5, 4.8, -9.6,-10.2
f = -3.4
print('type of f',type(f))
# type of f <class 'float'>

print()
# 3. complex - complex number contains a real part and an imaginary part
# In the below example, 5 is real part and 7j is imaginary part

c = 5 + 7j
print('Type of C',type(c))
# Type of C <class 'complex'>





