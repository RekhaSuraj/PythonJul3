# join
s1 = "Python is fun and easy to learn"
# Concatenate any number of strings.
# The string whose method is called is inserted in between each given string.
# The result is returned as a new string.
# separator - Joins and gives the string output for the list input, separator will be inserted to join the
# items in the list
# '.'.join(['ab', 'pq', 'rs']) -> 'ab. pq. rs'
s2 = " ".join(s1)
print(s2)
