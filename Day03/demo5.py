# Floor Division - Discards the decimal part from the quotient
# Syntax : //
a = 80
b = 3
print(a//b)
# 26
print(a/b)
# 26.666666666666668


print(8//2)
# 4

# Modulus - It returns remainder after division
# Syntax : %
x = 11
y = 2
print(x%y)

# Exponentiation - Power of 2 numbers
# syntax : **

a = 4
b = 2
print(a**b)
# 4 to the power of 2
# 16

print(5**3)
# 125
